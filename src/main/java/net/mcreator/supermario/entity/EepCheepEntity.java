	public static final EntityDataAccessor<String> ANIMATION = SynchedEntityData.defineId(EepCheepEntity.class, EntityDataSerializers.STRING);
	public static final EntityDataAccessor<String> TEXTURE = SynchedEntityData.defineId(EepCheepEntity.class, EntityDataSerializers.STRING);
	private AnimationFactory factory = GeckoLibUtil.createFactory(this);
	private boolean swinging;
	private boolean lastloop;
	private long lastSwing;
	public String animationprocedure = "empty";
	private static final Set<ResourceLocation> SPAWN_BIOMES = Set.of(new ResourceLocation("super_mario:mushroom_kingdom_ocean"), new ResourceLocation("super_mario:sparkling_waters_ocean"), new ResourceLocation("super_mario:sparkling_water_beach"));

	@SubscribeEvent
	public static void addLivingEntityToBiomes(BiomeLoadingEvent event) {
		if (SPAWN_BIOMES.contains(event.getName()))
			event.getSpawns().getSpawner(MobCategory.MONSTER).add(new MobSpawnSettings.SpawnerData(SuperMarioModEntities.EEP_CHEEP.get(), 9, 1, 1));
	}

	public EepCheepEntity(PlayMessages.SpawnEntity packet, Level world) {
		this(SuperMarioModEntities.EEP_CHEEP.get(), world);
	}

	public EepCheepEntity(EntityType<EepCheepEntity> type, Level world) {
		super(type, world);
		xpReward = 0;
		setNoAi(false);
		this.setPathfindingMalus(BlockPathTypes.WATER, 0);
		this.moveControl = new MoveControl(this) {
			@Override
			public void tick() {
				if (EepCheepEntity.this.isInWater())
					EepCheepEntity.this.setDeltaMovement(EepCheepEntity.this.getDeltaMovement().add(0, 0.005, 0));
				if (this.operation == MoveControl.Operation.MOVE_TO && !EepCheepEntity.this.getNavigation().isDone()) {
					double dx = this.wantedX - EepCheepEntity.this.getX();
					double dy = this.wantedY - EepCheepEntity.this.getY();
					double dz = this.wantedZ - EepCheepEntity.this.getZ();
					float f = (float) (Mth.atan2(dz, dx) * (double) (180 / Math.PI)) - 90;
					float f1 = (float) (this.speedModifier * EepCheepEntity.this.getAttribute(Attributes.MOVEMENT_SPEED).getValue());
					EepCheepEntity.this.setYRot(this.rotlerp(EepCheepEntity.this.getYRot(), f, 10));
					EepCheepEntity.this.yBodyRot = EepCheepEntity.this.getYRot();
					EepCheepEntity.this.yHeadRot = EepCheepEntity.this.getYRot();
					if (EepCheepEntity.this.isInWater()) {
						EepCheepEntity.this.setSpeed((float) EepCheepEntity.this.getAttribute(Attributes.MOVEMENT_SPEED).getValue());
						float f2 = -(float) (Mth.atan2(dy, (float) Math.sqrt(dx * dx + dz * dz)) * (180 / Math.PI));
						f2 = Mth.clamp(Mth.wrapDegrees(f2), -85, 85);
						EepCheepEntity.this.setXRot(this.rotlerp(EepCheepEntity.this.getXRot(), f2, 5));
						float f3 = Mth.cos(EepCheepEntity.this.getXRot() * (float) (Math.PI / 180.0));
						EepCheepEntity.this.setZza(f3 * f1);
						EepCheepEntity.this.setYya((float) (f1 * dy));
					} else {
						EepCheepEntity.this.setSpeed(f1 * 0.05F);
					}
				} else {
					EepCheepEntity.this.setSpeed(0);
					EepCheepEntity.this.setYya(0);
					EepCheepEntity.this.setZza(0);
				}
			}
		};
	}

	@Override
	protected void defineSynchedData() {
		super.defineSynchedData();
		this.entityData.define(SHOOT, false);
		this.entityData.define(ANIMATION, "undefined");
		this.entityData.define(TEXTURE, "eepcheeptexture");
	}

